package com.example.thinkingandtest.JavaBeanAndAdapter;

public class Fruit {

    private String name;
    private int icon;


    public Fruit(String name, int icon) {
        this.name = name;
        this.icon = icon;
    }

    public String getName() {
        return name;
    }

    public int getIcon() {
        return icon;
    }
}
